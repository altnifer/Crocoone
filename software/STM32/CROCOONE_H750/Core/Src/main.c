/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SDMMC2_SD_Init();
  MX_SPI2_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_UART7_Init();
  MX_UART8_Init();
  MX_FATFS_Init();
  MX_I2C1_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */

  HAL_GPIO_WritePin(POWER_ON_GPIO_Port, POWER_ON_Pin, GPIO_PIN_SET); //power on
  HAL_Delay(50);

  HAL_GPIO_WritePin(LCD_LIGHT_GPIO_Port, LCD_LIGHT_Pin, GPIO_PIN_RESET);
  ST7735_Init(1);
  fillScreen(MAIN_BG_COLOR);
  HAL_GPIO_WritePin(ESP_RESET_GPIO_Port, ESP_RESET_Pin, GPIO_PIN_SET);
  HAL_Delay(50);
  HAL_GPIO_WritePin(ESP_RESET_GPIO_Port, ESP_RESET_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LCD_LIGHT_GPIO_Port, LCD_LIGHT_Pin, GPIO_PIN_SET);

  ESP32_AT_UART_Init();
  Radio_Init();
  powerDown();
  ButtonsInit(5);
  AddButton(BUTTON_UP, BUTTON1_GPIO_Port, BUTTON1_Pin);
  AddButton(BUTTON_RIGHT, BUTTON2_GPIO_Port, BUTTON2_Pin);
  AddButton(BUTTON_CONFIRM, BUTTON3_GPIO_Port, BUTTON3_Pin);
  AddButton(BUTTON_LEFT, BUTTON4_GPIO_Port, BUTTON4_Pin);
  AddButton(BUTTON_DOWN, BUTTON5_GPIO_Port, BUTTON5_Pin);

  eeprom_init();

  int menu_set = 0;
  const int max_menu_set = 7;
  bool clear = 0;
  bool screen_update = 1;

  eButtonEvent button_up, button_down;
  uint32_t hold_timer = 0;

  char battery_persent_disp[10] = {};
  uint32_t tick_time = 0;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if (HAL_GetTick() - tick_time > 10000 || screen_update) {
			tick_time = HAL_GetTick();

			HAL_GPIO_WritePin(DIV_ENABLE_GPIO_Port, DIV_ENABLE_Pin, GPIO_PIN_RESET); //div enable

			HAL_ADC_Start(&hadc1);

			HAL_ADC_PollForConversion(&hadc1, 100);

			adcData = HAL_ADC_GetValue(&hadc1);

			HAL_ADC_Stop(&hadc1);

			HAL_GPIO_WritePin(DIV_ENABLE_GPIO_Port, DIV_ENABLE_Pin, GPIO_PIN_SET);

			batteryVoltage = POWER_SUPPLY_VOLTAGE * adcData / ADC_MAX;
			batteryVoltage = batteryVoltage / (DIVIDER_R2 / (DIVIDER_R1 + DIVIDER_R2));
			batteryPercent = (int)(((batteryVoltage - MIN_BATTERY_VOLTAGE) / (MAX_BATTERY_VOLTAGE  - MIN_BATTERY_VOLTAGE)) * 100.0f);
			if (batteryPercent < 0)
			  batteryPercent = 0;
			else if (batteryPercent > 100)
			  batteryPercent = 100;

			sprintf(battery_persent_disp, "%3d%%", batteryPercent);
			ST7735_WriteString(110, 0, battery_persent_disp, Font_11x18, MAIN_TXT_COLOR, MAIN_BG_COLOR);
		}

	    button_up = GetButtonState(BUTTON_UP);
	    button_down = GetButtonState(BUTTON_DOWN);

		if (button_down == SINGLE_CLICK || (button_down == HOLD && HAL_GetTick() - hold_timer > HOLD_MENU_DELLAY)) {
			menu_set = (menu_set + 1) % max_menu_set;
			screen_update = 1;

			hold_timer = HAL_GetTick();
		}
		if (button_up == SINGLE_CLICK || (button_up == HOLD && HAL_GetTick() - hold_timer > HOLD_MENU_DELLAY)) {
			menu_set = (menu_set - 1) % max_menu_set;
			if (menu_set < 0) menu_set = max_menu_set;
			screen_update = 1;

			hold_timer = HAL_GetTick();
		}

		if (clear) {
			fillScreen(MAIN_BG_COLOR);
			clear = 0;
		}

		if (screen_update) {
			sprintf(battery_persent_disp, "%3d%%", batteryPercent);
			ST7735_WriteString(0, 0, "CROCOONE", Font_11x18, MAIN_TXT_COLOR, MAIN_BG_COLOR);
			ST7735_WriteString(110, 0, battery_persent_disp, Font_11x18, MAIN_TXT_COLOR, MAIN_BG_COLOR);
			drawMenu((char * []) {"Scan WiFi", "Attacks", "Ch Analyzer", "Pkt Analyzer", "Jammer", "PMKID", "power off"}, max_menu_set, 1, 30, 159, 127, menu_set, Font_11x18, MAIN_TXT_COLOR, MAIN_BG_COLOR);
			drawRect(0, 29, 160, 99, MAIN_TXT_COLOR);
			screen_update = 0;
		}

		if (GetButtonState(BUTTON_CONFIRM) == SINGLE_CLICK) {
			clear = 1;
			if (menu_set == 0) {
				ESP32_scan_loop();
			} else if (menu_set == 1) {
				ESP32_attacks_loop();
			} else if (menu_set == 2) {
				NRF_ChannelAnalyzer_loop();
			} else if (menu_set == 3) {
				ESP32_pkt_analyzer_loop();
			} else if (menu_set == 4) {
				NRF_Jammer();
			} else if (menu_set == 5) {
				ESP32_start_pmkid_attack();
			} else if (menu_set == 6) {
				break;
			}
			ClearAllButtons();
			screen_update = 1;
		}

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  HAL_GPIO_WritePin(LCD_LIGHT_GPIO_Port, LCD_LIGHT_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(POWER_ON_GPIO_Port, POWER_ON_Pin, GPIO_PIN_RESET); //power off
  return 0;
  /* USER CODE END 3 */
}

